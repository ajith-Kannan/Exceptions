package day4;

public class CharParsing {
	public static void main(String[] args) {
		
		String a = args [0];
		String b = args [1];
		String c = args [2];
		
		char word1 = a.charAt(0);
		char word2 = b.charAt(0);
		char word3 = c.charAt(0);
		
		System.out.println(word1+"\n"+word2+"\n"+word3);
		
	}

}
