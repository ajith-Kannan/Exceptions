package conditons;

public class Month {
	public static void main(String[] args) {
		String month = "may";
		switch (month) {
		case "jan" : System.out.println(month+" is 1st month");
		break;
		case "feb" : System.out.println(month+" is 2nd month");
		break;
		case "mar" : System.out.println(month+" is 3rd month");
		break;
		case "apr" : System.out.println(month+" is 4th month");
		break;
		case "may" : System.out.println(month+" is 5th month");
		break;
		case "jun" : System.out.println(month+" is 6th month");
		break;
		case "july" : System.out.println(month+ "is 7th month");
		break;
		case "aug" : System.out.println(month+ "is 8th month");
		break;
		case "sep" : System.out.println(month + "is 9th month");
		break;
		
		
		}
		
	}

}
