package oops1;

public class Compressor {
	private int capacity;
	private String phaseType;
	private int price;
	
	public void setCapcity (int a) {
		capacity = a;
		
	}
	public int getCapacity() {
		return capacity;
	}
	public void setPhaseType(String b) {
		phaseType = b;
	}
	public String getPhaseType() {
		return phaseType;
	}
	public void setPrice (int c) {
		price = c;
	}
	public int getPrice() {
		return price;
	}
	
	

}
